package learning_the_basic.Arrays;

public class ArrayInicializacaoRapida {
    public static void main(String[] args) {
        // Array de Strings inicializado diretamente
        String[] frutas = {"Maçã", "Banana", "Laranja", "Uva"};

        // Array de doubles
        double[] precos = {1.99, 0.99, 2.49, 3.99};

        System.out.println("Segunda fruta: " + frutas[1]);
        System.out.println("Terceiro preço: " + precos[2]);
    }
}
